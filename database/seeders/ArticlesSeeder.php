	<li>соматический (генотип, уровень обмена веществ, уровень физического развития, тип конституции, функциональное состояние и резервные возможности системы организма шахматиста);</li>
	<li>психологический (эмоционально-волевая и интеллектуальная сферы шахматиста, темперамент);</li>
	<li>социально-духовный (целевые установки шахматиста, его нравственные ценности, идеалы, актуальные потребности, степень признания);</li>
</ul>
&nbsp;

Например, игры в шахматы  способствуют уменьшению признаков старческого слабоумия. Играя в шахматы  долгие годы, старики становятся активнее и общительнее.

Шахматная стимуляция мозга активизирует связи между нейронами и предохраняет их от деградации. Большинство психологических исследований, начиная с исследований французского психолога А.Бине (1894 год), который изучал особенности мышления, памяти и одарённости шахматистов, посвящено их интеллектуальным функциям

(А. Кливленд, Б.Блюменфельд, О.Тихомиров,А.де Грот и др.), но в шахматной игре важная роль отводится и личности в целом: «Шахматы не только соревнование умов, но и интеллектуальное состязание характеров» (Б.Ананьев).

Психология здоровья опирается на подход через раскрытие потенциала шахматиста:
<ul>
	<li>потенциал разума (интеллектуальный аспект здоровья) – способность шахматиста развивать интеллект и уметь им пользоваться.</li>
	<li>Потенциал воли (личностный аспект здоровья) - способность шахматиста ставить цели и достигать их не только на шахматной доске, но и в жизни, выбирая адекватные средства.</li>
	<li>Потенциал чувств (эмоциональный аспект здоровья)- способность шахматиста выражать свои чувства в результате победы или поражения в партии, также как в жизненной борьбе, понимать подобное состояние и принимать чувства других.</li>
	<li>Потенциал тела (социальный аспект здоровья) - способность шахматиста адаптироваться к социальным условиям, вырабатывать чувство принадлежности к обществу - «Мы все – одна семья»</li>
</ul>
Многие психологи сравнивают человеческую память с картотекой. Эта аналогия подразумевает, что вы извлекаете из окружающего мира информацию на какую-либо тему и затем храните её в своём мозге. Но хотя точное местонахождение памяти учёным и неизвестно, они вполне единодушны в том, что касается её объёма. Люди используют лишь малую часть возможностей своей памяти. Развитие памяти, несомненно, весьма достойная цель. Начать следует с изучения шахмат.

Например, кратковременная память сохраняет информацию в течение ограниченного времени в ограниченном объёме. Объединение отдельных единиц информации с целью экономии места для хранения  дополнительных сведений называется группировкой. Методом группировки часто пользуются сильные шахматисты. Бросив беглый взгляд на доску,  они видят не отдельные шахматные фигуры, а комбинации. Посмотрев на доску в течение всего двух- трёх секунд, хороший игрок может в точности воспроизвести позицию. Ясно, что человек с обычной кратковременной памятью не сможет так быстро запомнить расположение 32 шахматных фигур на 64-клеточной доске.

«Депрессия от успехов» как особое психическое состояние шахматиста. Причина депрессии порой заключается в специфических переживаниях успеха. Такое состояние может возникнуть, когда к успеху вела тяжёлая дорога, и за него пришлось заплатить высокую цену. При достижении заветной цели в шахматной карьере нередко возникают переживания грусти, опустошённости, растерянности, но они, как правило, носят кратковременный характер. Однако, если восхождение было долгим и человеку пришлось отказаться от возможностей саморазвития, от многих простых радостей бытия, приятных переживаний, то состояние может стать острым.

Стресс (от англ.stress – нажим, давление) это любое более или менее выраженное напряжение организма, связанное с его жизнедеятельностью. И в этом качестве стресс - неотъемлемая  часть проявлений жизни. Любая нормальная деятельность-игра в шахматы или выступление на конференции может вызвать стресс, не причинив никакого вреда. Стресс не привязан к строго определённой группе трудных состояний, но как непременный атрибут жизнедеятельности  он  может дать начало любому из них.

К числу наиболее интересных, поучительных ценных в воспитательном и пропагандистском плане глав истории шахмат принадлежат те, в которых шахматы соприкасаются с другими сферами творческой деятельности человека, и в первую очередь с наукой. Давно подмечено, что шахматы во многом близки, родственны науке. Не случайно среди корифеев шахмат так много людей, ярко проявивших себя в науке. С другой стороны, многие корифеи науки из всевозможных занятий  в часы отдыха предпочтение отдавали шахматам.

Было бы не совсем правильно утверждать, что все без исключения учёные положительно относятся к шахматам. Среди деятелей науки встречаются не только друзья шахмат, но и противники. Последние, как правило, считают,  что увлечение шахматами отвлекают учёного от его основных занятий. И всё же в большинстве своём учёные оценивают шахматы весьма положительно, справедливо полагая, что эта игра позволяет развить многие ценные качества, важные для успешной научной деятельности.

Шахматы привлекают внимание учёных многообразием возникающих на шахматной доске ситуаций, представляющих неисчерпаемые возможности для  проявления творчества. В отличие от практической деятельности , когда  мгновения творчества распределены во всём многообразии жизненных ситуаций, при игре в шахматы необходимость в творческом поиске возникает почти на каждом ходу. Радость победоносных озарений и горечь роковых ошибок преследуют шахматиста постоянно. Весьма схож и механизм мышления, приводящего к открытию в науке валеологии и нахождению лучшего продолжения в шахматной партии.

Насколько же глубоки и содержательны шахматы, сколь велики их возможности, как  широко они разработаны, если использование лишь ничтожной доли всего арсенала сил позволяет показать так много из  их несметного богатства!.Как в капле  воды отражается многообразие  мира, так и в этой крупице материала сверкают шахматы различными своими гранями…

