- Спасибо, я тоже надеюсь.

<strong>- А в чайхану «Рохат» ходите по-прежнему?</strong>

- Конечно, мне же знаком там каждый кирпич. Хотя, в шахматы больше никто не играет, да и вкус лагмана, если честно, уже не тот…

