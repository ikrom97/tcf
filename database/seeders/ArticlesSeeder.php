<strong>- Хорошо, давайте вернемся к шахматам. В каких турнирах Мунир выступил за последнее время?</strong>

- После того как мы вернулись  в Душанбе, Мунир участвовал в полуфинале чемпионата Таджикистана среди мужчин и в 11 лет выполнил норму кандидата в мастера спорта. В данное время он занимается в группе мастерства у тренера ФИДЕ мастера спорта СССР Рашида Хесейнова, вместе с ведущими молодыми шахматистами –НасриддинзодаМарвори, Антоновой Надеждой, Каримовым Алишером, МутрибаиИлхом и Кабиловым. Перед ним поставлена задача в ближайшее время войти в число ведущих шахматистов республики.

<strong>- По мнению многих шахматных специалистов, большинство чемпионов и ведущих гроссмейстеров не достигли бы значительных результатов без каждодневной поддержки и опеки близких им людей. Мать Каспарова и Карякина, отец Карлсена, Камского и Раджабова, список можно продолжать бесконечно.</strong>

- Да, вы правы, и меня вдохновил пример отца сестер Полгар, который ради достижения своими детьми спортивных результатов пожертвовал очень многим, даже личным счастьем.

<strong>- Сухроб, в конце нашей беседы хотелось бы вам пожелать стойкости и терпения в вашем нелегком поприще, а Муниру новых спортивных свершений.</strong>

